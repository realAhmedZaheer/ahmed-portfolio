/** New piece centered horizontally, at the top of the buffer. */
export function spawnPiece(kind: PieceKind): Piece {
  return { kind, rot: 0, x: 3, y: 0 };
}

export function kickKey(from: Rot, to: Rot): keyof typeof KICKS.JLSTZ {
  return `${from}>${to}` as keyof typeof KICKS.JLSTZ;
}
